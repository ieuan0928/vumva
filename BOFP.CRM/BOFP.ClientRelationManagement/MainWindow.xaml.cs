namespace BOFP.ClientRelationManagement
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void ToolBar_Loaded(object sender, RoutedEventArgs e)
        {
            ToolBar toolBar = sender as ToolBar;
            FrameworkElement overflowGrid = toolBar.Template.FindName("OverflowGrid", toolBar) as FrameworkElement;
            if (overflowGrid != null)
            {
                overflowGrid.Visibility = Visibility.Collapsed;
            }

            FrameworkElement mainPanelBorder = toolBar.Template.FindName("MainPanelBorder", toolBar) as FrameworkElement;
            if (mainPanelBorder != null)
            {
                mainPanelBorder.Margin = new Thickness(0);
            }
        }

        private void ToolBarRadio_CheckedChanged(object sender, RoutedEventArgs e)
        {
            if (!IsLoaded) return;

            if (clientToolBarRadioButton.IsChecked.Value) mainTabControl.SelectedItem = clientTabItem;
            else if (transactionToolBarRadioButton.IsChecked.Value) mainTabControl.SelectedItem = transactionsTabItem;
        }
    }
}
